package leetcode.arrays.marge_sorted_arrays.pages.arrays;

import java.util.Arrays;

public class ArrayFirst {
    private String [] arrayFirst;

    public String[] getArrayFirst() {
        return arrayFirst;
    }

    public void setArrayFirst(String[] arrayFirst) {
        this.arrayFirst = arrayFirst;
    }

    @Override
    public String toString() {
        return "ArraySecond{" +
                "arrayFirst=" + Arrays.toString(arrayFirst) +
                '}';
    }
}
